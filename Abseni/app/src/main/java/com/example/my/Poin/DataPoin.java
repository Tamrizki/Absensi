package com.example.my.Poin;

public class DataPoin {
    int jumlah_poin;
    String key;

    public DataPoin() {
    }

    public DataPoin(int jumlah) {
        this.jumlah_poin = jumlah;
    }

    public int getJumlah_poin() {
        return jumlah_poin;
    }

    public void setJumlah_poin(int jumlah_poin) {
        this.jumlah_poin = jumlah_poin;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }
}
